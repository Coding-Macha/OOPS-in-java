package Polymorphism;
/*
 Polymorphism: Performing single action in different ways is called as polymorphism.
 			   Its a Greek word. where Poly means many and morph means forms.
 			   Having many forms.
 				
 There are two types of polymorphism in java:
1) Static Polymorphism 		(compile time polymorphism)
2) Dynamic Polymorphism 	(runtime polymorphism)


Static PolyMorphism:

Polymorphism that is resolved during compiler time is known as static polymorphism. 
Method overloading is an example of compile time polymorphism

Overloading means  multiple methods having same name but different in parameters

There are two ways to overload the method in java

By changing number of arguments
By changing the data type




OverRiding:
subclass (child class) has the same method as declared
in the parent class, it is known as method overriding in Java

Rules for Java Method Overriding
1. The method must have the same name as in the parent class
2. The method must have the same parameter as in the parent class.
3. There must be an IS-A relationship (inheritance).
 */

